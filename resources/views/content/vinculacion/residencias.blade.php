      <p><h3>¿QUE ES LA RESIDENCIA PROFESIONAL?</h3></p>

      <p align="justify">Se considera residencia profesional a aquella actividad, realizada durante el desarrollo de un proyecto o en la aplicación práctica de un modelo, en cualquiera de las áreas de desarrollo establecidas, que definan una problemática y propongan una solución viable, a través de la participación directa del alumno en desempeños propios de su futura profesión.<br>

        Las <strong>Residencias Profesionales</strong> se plantean como un proceso de aprendizaje que implica la aplicación del conocimiento teórico adquirido en las aulas con diversas experiencias prácticas a las cuales propondrá respuesta dentro del ámbito laboral.
      </p>


      <p><h3>DURACION</h3></p>

      <p align="justify">Su duración queda determinada por un período de 4 meses como tiempo mínimo y 6 meses como tiempo máximo, debiendo acumularse un mínimo de 500 horas.
        La asignación de proyectos de residencia profesional, se debe realizar en periodos previos a la elección de la carga académica del inicio al periodo escolar
      </p>


      <p><h3>REQUISITOS:</h3></p>

      <p align="justify">Los alumnos(as) que soliciten la asignación oficial de su proyecto de residencia profesional, por cualquiera de las opciones antes mencionadas, deberán presentarse en la División de estudios profesionales o área correspondiente, donde entregarán:  <br>
        a) Tener aprobado al menos el 80% de créditos de su plan de estudio.<br>
        b) Tener acreditadas las actividades complementarias.<br>
        c) Tener acreditado el servicio social.<br>
        d) No contar con ninguna asignatura en condiciones de “Curso Especial”
      </p>


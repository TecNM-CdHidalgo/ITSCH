				<p align="center" style="font-size:23px;"><strong>¿QUIÉNES SOMOS?</strong></p>

				<p align="justify"> El Centro de Incubación e Innovación Empresarial del Tecnológico Nacional de México,
					Campus Ciudad Hidalgo, tiene por objetivo fundamental contribuir al desarrollo económico, social y
					cultural de los emprendedores en el estado, para que de acuerdo a sus propias características, los
					miembros que la conforman, promuevan proyectos productivos y actividades que permitan fomentar y
					consolidar la cultura emprendedora, el intercambio de experiencias exitosas, el establecimiento de
					mecanismos de comunicación y la gestión con organizaciones locales, regionales, nacionales e
					internacionales.</p>
				<p>
					<span class="media thumbnail pull-left"><img src="./media/vinculacion/negocios/slide.jpg" width="210" height="300"></span>
				</p>

				<p align="center" style="font-size:23px;"><strong>MODELO DE INCUBACIÓN DE EMPRESAS DEL TECNOLÓGICO NACIONAL DE MÉXICO CAMPUS CIUDAD HIDALGO</strong></p>
				<p>
					<!--aqui la nueva imagen del documento para actualizar-->
					<span class="media thumbnail" style="margin-bottom:30px;"><embed class="img-fluid" src="./vinculacion/img/incubadoras/1.jpg"></span>
					<br>
				</p>
				<li>
					<strong>Scouting.</strong> Buscando ideas emprendedoras. Tienen como objetivo identificar emprendedores e ideas de emprendimiento factibles de realizarse. La fase tiene una duración de 30 días naturales.
				</li>
				<li>
					<strong>Creating.</strong> Creando tu negocio. Durante esta fase se desarrolla el modelo y el plan de negocios de la empresa. La fase tiene una duración de 60 a 90 días naturales.
				</li>
				<li>
					<strong>Tracing.</strong> Siguiendo tus logros. La fase considera el seguimiento de los indicadores que permiten a una empresa conocer su desempeño a través de identificar los empleos generados, empleos conservados, las ventas, etc. La fase considera 365 días para obtener resultados significativos de una empresa de nueva creación.
				</li>
				<br> <br>

				<p align="center" style="font-size:23px;"><strong>SERVICIOS QUE OFRECEMOS</strong></p>

				<br>
				<h4>CIIE TecNM Campus Ciudad Hidalgo</h4>
				<br>

				<li>Brindamos servicio de asesoría para la realización de modelos y planes de negocios.</li>
				<li>Asesoría técnica, financiera, mercadotécnica, contable y legal durante el proceso de incubación.</li>
				<li>Se imparten cursos y talleres de capacitación a empresas de acuerdo a sus necesidades.</li>
				<li>Participamos en convocatorias para emprendedores emitidas por la Secretaria de Desarrollo Económico (SEDECO), Instituto de Ciencia Tecnología e Innovación (ICTI) y Fondo Nacional del Emprendedor (FNE).</li>
				<li>Para los estudiantes se imparte semestralmente el taller Modelo Talento Emprendedor con la finalidad de fomentar la cultura emprendedora y desarrollar ideas de negocio.</li>
				<li>El centro de incubación brinda el servicio de asesoría para realizar registros de la propiedad intelectual, marcas, patentes y registro de obras, ante el Instituto Mexicano de la Propiedad Industrial (IMPI) y el Instituto Nacional del Derecho de Autor (INDAUTOR).</li>


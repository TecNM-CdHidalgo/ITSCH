                 <p><h5><strong>INGRESO Y SELECCIÓN DE ALUMNOS DE NUEVO INGRESO.</strong></h5></p>



     <p align="justify">Procedimiento para el ingreso y selección de los nuevos alumnos que ingresan al Instituto Tecnológico Superior de Ciudad Hidalgo </p>


       <hr><hr>
                 <p align="justify">Ingreso y selección de los alumnos de nuevo ingreso</p>
                 <hr>

                 <h2>Objetivo</h2>
                    <p align="justify">Seleccionara a los mejores candidatos para ingresar a estudiar una carrera profesional en el Instituto Tecnológico Superior de Ciudad Hidalgo, que sean jóvenes que tengan gran potencial para convertirse en lideres sin fronteras con sentido humando y profesionales en el área de ingeniería que puedan ser factores de cambio en su entorno. </p>
                     <h2>Responsables</h2>
                       <p align="justify">Las áreas responsables de dar cumplimiento a este procedimiento son la Subdirección Académica, la Subdirección de Vinculación, el jefe del Departamento de Servicios Escolares y la Subdirección de investigación y Posgrado. </p>

                       <p>Pasos a seguir:</p>
                               1.- Se hace la proyección de la cantidad de grupos de primer semestre para el ciclo escolar que continua.    <br>
                                2.- Se determina la cantidad de fichas que se entregaran por carrera y la fecha del examen de admisión. Los requisitos para obtener la ficha son copia de acta de nacimiento, copia de la CURP, constancia de estudios o copia de certificado de bachillerato, pago correspondiente y 2 fotografías tamaño infantil.    <br>
                                 3.- Se inicia el proceso de entrega de fichas y en conjunto con el Departamento de desarrollo académico se aplica el examen de admisión.    <br>
                                  4.- Cuando se reciben los resultados del examen de admisión , la subdirección académica en conjunto con los jefes de división definirán a los alumnos seleccionados de la carrera y la Subdirección académica publicara las listas de aceptados.
                           <br>
                                   5.- la cantidad de alumnos seleccionados se generaran a partir de los siguientes criterios:
                           <ol>	1.- Espacios y cantidad de grupos definidos. </ol>
                              <ol>2.- Mejores calificaciones en el examen de admisión.</ol>
                                 <ol>3.- Mejor promedio de bachillerato. </ol>



               <br>

                          <p align="justify">6.- una vez publicadas las listas se publican las fechas del curso de de inducción propedéutico así como los pasos a seguir para el ingreso al instituto par parte de servicios escolares.</p>
                      <br>
                             <p align="justify">7.- los alumnos toman el curso de inducción y propedéutico y se inscriben según las fechas y requisitos publicados por servicios escolares</p>













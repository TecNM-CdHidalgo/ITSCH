<h3>5.	DIPLOMADO DE INGLÉS</h3>

<p>Descripción:</p>
<li>El programa de inglés que actualmente tenemos en el ITSCH consta de 8 niveles, cada nivel comprende 64 horas de estudio que pueden ser cursadas en periodos semestrales y/o en cursos de verano. Las clases son de 4 horas a la semana.</li>
<li>En cada nivel se aplican 4 evaluaciones parciales y al final del periodo se promedian los resultados parciales para obtener la calificación final del nivel, si esta calificación es 80 o más alta se aprueba al siguiente nivel, de lo contrario, el estudiante tendrá que repetir el nivel hasta aprobarlo.  </li>





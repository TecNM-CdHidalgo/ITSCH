    <h3> II. ¿Para qué fines recabamos y utilizamos sus datos personales?</h3>
    <h4>Las finalidades del tratamiento de sus datos personales son:</h4>
    <ol style="list-style-type:lower-alpha">
        <li>Ficha de examen de admisión; e,</li>
        <li>Titulación; e,</li>
        <li>Elaboración de Constancias con y sin calificaciones; e,</li>
        <li>Certificado parcial; e,</li>
        <li>Duplicado de credencia; e,</li>
        <li>Inscripción al Seguro Social; e,</li>
        <li>Baja definitiva; e,</li>
        <li>Examen global de conocimiento; e,</li>
        <li>Tramite Tardío; e,</li>
        <li>Préstamo de documentos; e,</li>
        <li>Verano materia teórica; e,</li>
        <li>Kardex; e,</li>
        <li>Examen EGEL.</li>       
    </ol>
    <h3>III. ¿Qué datos personales obtenemos?</h3>
    <h4>Para cumplir las finalidades anteriores requerimos dependiendo del procedimiento o
        actividad a realizar de los siguientes datos personales:</h4>
    <ol style="list-style-type:lower-alpha">
        <li>De identificación:
            Nombre, Origen Étnico y Racial, Lengua Materna, Domicilio, Teléfono, Correo
            Electrónico, Firma, Contraseñas, RFC, CURP, Fecha de Nacimiento, Edad,
            Nacionalidad, Estado Civil.</li>
        <li>De Educación:
            Escuelas, Calificaciones, Títulos, Cédulas, Certificados, Diplomas.</li>
        <li>De Salud:
            Estado de Salud, Historial y Estudios Clínicos, Enfermedades, Tratamientos Médicos,
            Medicamentos, Alergias, Embarazos, Condición Psicológica y/o Psiquiátricas.
            Usted tiene la facultad de determinar si los entrega o no.</li>
    </ol>
    <h3> IV. Fundamento Legal.</h3>
    <p> En lo que se refiere al tratamiento de sus datos personales, el responsable lo efectuara en
        términos del numeral 23, 24 y demás de la Ley de Protección de Datos Personales en
        Posesión de Sujetos Obligados del Estado de Michoacán de Ocampo.</p>
    <h3> V. Mecanismos de seguridad.</h3>
    <p>En el Instituto Tecnológico Superior de Ciudad Hidalgo se emplean procedimientos físicos
        para prevenir el acceso no autorizado, mantener la exactitud de los datos, y garantizar el
        uso correcto de su información personal.</p>
    <h3>VI. ¿Cómo Acceder, ¿Rectificar, Cancelar u Oponerse al uso y tratamiento de
        sus datos personales (Derechos ARCO) o revocar su consentimiento para el
        tratamiento sus datos?</h3>
    <p>Usted tiene derecho de acceso, rectificación, cancelación u oposición al tratamiento de sus
        datos personales o revocar el consentimiento. Para el ejercicio de estos derechos el titular
        de los datos personales o su representante deberán presentar solicitud de ejercicio de
        derechos ARCO, misma que podrá ser presentada en formato libre siempre que reúna los
        siguientes requisitos:</p>
    <ol style="list-style-type:lower-alpha">
        <li>Acreditar que es el titular de los datos personales ante la autoridad a la que se dirige
            la solicitud.</li>
        <li>Nombre, datos generales e identificación oficial del solicitante, o en su defecto poder
            otorgado por el titular de los datos personales.</li>
        <li>Precisión de los datos respecto de los que busca ejercer alguno de los derechos
            ARCO (Acceso, Rectificación, Cancelación y Oposición).</li>
        <li>Domicilio para recibir notificaciones y/o correo electrónico.</li>
        <li>Modalidad en la que prefiere se les otorgue el acceso a sus datos (verbalmente,
            mediante consulta directa, a través de documentos como copias simples,
            certificadas u otros).</li>
        <li>Algún elemento que facilite la localización de la información.</li>
        <li>Firma del solicitante.</li>
    </ol>
    <p> Le informamos que puede presentar su solicitud de protección de datos personales vía
        electrónica a través de la Plataforma Nacional de Transparencia</p>
    <a href="http://www.plataformadetransparencia.org.mx" target="blank">http://www.plataformadetransparencia.org.mx</a>
    <p> o bien puede acudir directamente a las
        oficinas de la Unidad de Transparencia del Instituto Tecnológico Superior de Ciudad

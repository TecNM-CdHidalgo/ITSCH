    <h2>AVISO DE PRIVACIDAD DE ACCESO A LA INFORMACIÓN</h2>
    <h3>I. Responsable de la protección de sus datos personales.</h3>
    <p>        
        El Instituto Tecnológico Superior de Ciudad Hidalgo con Unidad de Transparencia,
        ubicada en Av. Ing. Carlos Rojas Gutiérrez #2120 Fracc. Valle de la Herradura, C.P. 61100,

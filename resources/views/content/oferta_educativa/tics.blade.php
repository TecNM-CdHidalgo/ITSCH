            <h2>¿QUÉ ES UN INGENIERO EN TIC´s?</h2>

            <hr class="red">
            <p align="justify">
                El  Ingeniero en Tecnologías de la Información y Comunicaciones </strong>es un profesional que se enfoca a la integración, implantación, mantenimiento, operación y administración de soluciones sustentadas en computación, electrónica y sistemas de información, integradas al servicio de personas y organizaciones. Es un profesionista líder, con visión innovadora, que aplica tecnología de vanguardia para brindar soluciones tecnológicas sustentables a las organizaciones en la nueva y dinámica sociedad de la era digital.</p>
            <hr>


            <h2>VISION:</h2>

            <hr class="red">

            <p align="justify">
                 <i>“Ser un programa educativo de vanguardia, acreditado y reconocido por su alta calidad académica en la formación de profesionistas en tecnologías de la información y comunicaciones, que sean capaces de desarrollar el sector tecnológico de su entorno de manera sustentable”.</i></strong>

                <br> <br>

                Con esta visión el Programa Educativo busca contribuir a la transformación de la región, y particularmente de la Educación Superior Tecnológica en México, orientando sus esfuerzos hacia el desarrollo humano sustentable y la competitividad.

            </p>



            <h2>MISION:</h2>

            <hr class="red">
            <p align="justify">
                 <i>“Formar profesionistas capaces de integrar y administrar tecnologías de la información y comunicaciones, que contribuyan a la productividad y el logro de los objetivos estratégicos de las organizaciones; caracterizándose por ser líderes, críticos, competentes, éticos y con visión empresarial, comprometidos con el desarrollo sustentable”.</i></strong>



                <br>

                Con esta misión el Programa Educativo busca formar profesionales capaces de influir en su medio ambiente, transformarlo y aplicar tecnología que contribuya al desarrollo regional.

            </p>
            <hr>


           <h2>PERFIL DE INGRESO:</h2>

            <hr class="red">

            <p align="justify">
                Las características básicas que se requieren para que el aspirante pueda tener un mejor desempeño en su proceso de formación profesional son los siguientes:


            <li>Poseer conocimientos, habilidades y aptitudes que le permitan un paso sólido durante su estancia y una conclusión exitosa del Plan de Estudios.</li>
            <li>Tener conocimiento a nivel medio superior de ciencias básicas: Matemáticas, Física y Química.</li>
            <li>Poseer creatividad e ingenio, así como una mentalidad analítica critica.</li>
            <li>Manejar los instrumentos elementales de medición.</li>
            <li>Tener el hábito de resolver, criterios de decisión y un manejo adecuado de las Relaciones Humanas.</li>


            <br>

            De igual manera, nuestros egresados deben contar con el siguiente perfil después de haber cursado el plan de estudios.

            </p>
            <hr>

           <h2> PERFIL DE EGRESO: </h2>

            <hr class="red">


            <p align="justify">


            <li>Desempeñar funciones de consultoría y auditoría en el campo de las Tecnologías de la Información y Comunicaciones.</li>

            <li>Crear empresas en el ámbito de las Tecnologías de la Información y Comunicaciones.</li>

            <li>Diseñar, implementar y administrar redes de cómputo y comunicaciones, bajo modelos y estándares internacionales, para satisfacer las necesidades de información de los sistemas sociales, garantizando aspectos de seguridad y calidad.</li>
            <li>Administrar proyectos que involucren Tecnologías de la Información y Comunicaciones en las organizaciones conforme a requerimientos establecidos.</li>
            <li>Diseñar, desarrollar y mantener sistemas de bases de datos asegurando la integridad, disponibilidad y confidencialidad de la información almacenada.</li>
            <li>Integrar las diferentes arquitecturas de hardware y administrar plataformas de software para incrementar la productividad en las organizaciones.</li>
            <li>Implementar sistemas de seguridad bajo políticas internas de las organizaciones y estándares aceptados.</li>
            <li>Observar los aspectos legales del uso y explotación de las Tecnologías de la Información y Comunicaciones.</li>
            <li>Desarrollar e implementar sistemas de información para el control y la toma de decisiones utilizando metodologías basadas en estándares internacionales.</li>
            <li>Analizar, diseñar y programar dispositivos con software empotrado.</li>

            <li>Utilizar tecnologías y herramientas actuales y emergentes acordes a las necesidades del entorno.</li>





            </p>



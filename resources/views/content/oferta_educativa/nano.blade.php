        <p align="center" style="font-size:20px;"><strong>Laboratorio e infraestructura</strong></p>
        <p align="justify">
            Laboratorio de nanotecnología está orientado a la síntesis y caracterización de materiales,
            los cuales generalmente están constituidos por estructuras a nivel micro y nano métrico.
            Dicho laboratorio cuenta con equipamiento que permite satisfacer la mayoría de las necesidades
            de los estudiantes durante su estancia en la carrera y si así lo deciden para realizar residencias profesionales/ Tesis.
        </p>
        <p align="justify">
            El laboratorio permite realizar síntesis de diversos materiales mediante técnicas como sol-gel, método de Pechini,
            precipitación- coprecipitación, método hidrotermal, deposición por baño químico, método de impregnación, entre otras.
            Esta área cuenta con lo necesario para realizar dichas síntesis, tales como muflas de altas temperaturas, baño ultrasónico,
            balanza analítica, parrillas de agitación con temperatura, reactivos necesarios, material de laboratorio, etc.
        </p>
        <p align="justify">
            Además, contamos con convenios de colaboración que permiten al estudiante manipular equipos especializados en el área.
        </p>

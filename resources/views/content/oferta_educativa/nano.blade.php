        <hr class="red">
        <p align="Justify">
            Un ingeniero nanotecnólogo se encarga de diseñar y desarrollar diversos materiales y herramientas a escala nanométrica,
            es decir, a las mil millonésimas partes de un metro, donde los materiales que se manipulan atómica y molecularmente confieren
            nuevas propiedades superiores y novedosas a las que se tenían inicialmente de forma convencional. Este profesional tiene la
            capacidad de ocupar cargos en laboratorios de investigación, de desarrollo e innovación tecnológica tanto del sector educativo
            como industrial, en las ramas de industrias cementeras, de estructuras químicas, productos de construcción, generación de nuevas
            fuentes de energía, de mejoramiento y protección ambiental, aseguramiento de calidad del diseño y evaluación de fármacos y
            biomateriales, producción de nanosensores y de materiales nano estructurados.
        </p>

        <h2>CÁPSULAS DE LOS EGRESADOS  </h2>
        <hr class="red">
        <ul>
            <li>         El egresado puede desempeñarse en:    </li>

            <li>        Industria de fermentaciones (alcohólica, láctica, producción de ácidos, antibióticos, entre otros).   </li>

            <li>        Industria extractiva (extractos de productos naturales).    </li>
            <li>       Industria alimentaria (alimentos procesados, frescos, métodos de empaque).     </li>
            <li>       Diseño de procesos de transformación de productos bióticos.     </li>
            <li>        Institutos de investigación (biológico, bioquímico, químico, ingeniería entre otros).   </li>

            <li>    Escuelas de educación superior y media superior. </li>

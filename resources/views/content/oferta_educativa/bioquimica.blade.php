        <h2>¿QUÉ ES LA INGENIERÍA BIOQUÍMICA? </h2>
        <hr class="red">

        <p align="Justify">
            Es una profesión que transforma e industrializa los sistemas biológicos como plantas, animales y microorganismos de forma inteligente, así como los productos (con actividad biológica) del metabolismo de éstos.  </p>
        <br>

        <h2>MISIÓN DE LA CARRERA DE ING. BIOQUÍMICA </h2>
        <hr class="red">

        <p align="justify">
            Formar recursos humanos en el área químico-biológica, capaces de fortalecer e incrementar el desarrollo científico y tecnológico, utilizando las herramientas de la ingeniería para solventar problemáticas actuales a nivel regional, estatal y nacional con un enfoque sustentable.         </p>
        <br>

        <h2>VISIÓN DE LA CARRERA DE ING. BIOQUÍMICA  </h2>
        <hr class="red">
        <p align="justify">
            Ser un programa educativo innovador y pertinente, con reconocimiento nacional e internacional, que contribuya al desarrollo y mejoramiento del sector productivo, científico y educativo, respaldado por personal académico de alta calidad, orientado a formar profesionales con sentido de responsabilidad social y conciencia ambiental.         <br>

        <h2>POLÍTICA DE LA CARRERA DE ING. BIOQUÍMICA </h2>
        <hr class="red">
        <p align="justify">
            La carrera de Ingeniería Bioquímica forma profesionales líderes en el área Químico – Biológica que generan soluciones prácticas a problemáticas reales en el sector productivo, educativo y científico.
        </p>
        <br>

        <h2>OBJETIVO GENERAL </h2>
        <hr class="red">
        <p align="justify">
            Formar profesionales íntegros de la Ingeniería Bioquímica competentes para trabajar en equipos multidisciplinarios y multiculturales que, con sentido ético, crítico, creativo, emprendedor y actitud de liderazgo, diseñe, controle, simule y optimice equipos, procesos y tecnologías sustentables que utilicen recursos bióticos y sus derivados, para la producción de bienes y servicios que contribuyan a elevar el nivel de vida de la sociedad.
        </p>
        <br>

        <h2>OBJETIVO DE LA ESPECIALIDAD </h2>
        <hr class="red">
        <p align="justify">
            El Ingeniero Bioquímico Biotecnólogo será capaz de desarrollar aplicaciones tecnológicas empleando los sistemas biológicos y organismos vivos o sus derivados para la generación o modificación de productos o procesos en el sector productivo y científico en el país.
        </p>
        <br>


        <h2>PERFIL DE INGRESO </h2>
        <hr class="red">
        <p align="justify">
            El aspirante a ingresar al programa de Ingeniería Bioquímica, deberá preferentemente haber cursado Bachillerato de Ingeniería, Químico-Biológico o afines y cubrir las siguientes características:         </p>

        <br>

        <ol>


            <li>   Tener conocimientos sólidos en las ciencias básicas como química, biología y matemáticas.</li>

            <li>   Tener interés por las ciencias biológicas como la bioquímica, microbiología, biología molecular, biotecnología; así como su aplicación en situaciones reales.</li>

            <li>     Tener capacidad de observación, análisis y abstracción de información.</li>

            <li>   Tener sentido creativo, emprendedor, proactivo, ético y responsable con su entorno ambiental y social.</li>

            <li>   Asumir el compromiso y responsabilidad con el estudio y desarrollo de proyectos, en armonía con los valores del Tecnológico Nacional de México campus Ciudad Hidalgo.</li>
        </ol>




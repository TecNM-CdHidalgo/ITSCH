        <h2>ATRIBUTOS DE EGRESO </h2>
        <hr class="red">
        <ul>
            <li>    Identificar, formular y resolver problemas complejos de ingeniería aplicando los principios de las ciencias básicas e ingeniería.  </li>

            <li>  Aplicar, analizar y sintetizar procesos de diseño de ingeniería que resulten en proyectos que cumplan las necesidades especificadas.  </li>

            <li>  Desarrollar y conducir una experimentación adecuada; analizar e interpretar datos y utilizar el juicio ingenieril para establecer conclusiones.  </li>

            <li>  Comunicarse efectivamente con diferentes audiencias.  
            </li>
            <li>   Reconocer sus responsabilidades éticas y profesionales en situaciones relevantes para la ingeniería y realizar juicios informados, que consideren el impacto de las soluciones de ingeniería en los contextos global, económico, ambiental y social.  </li>

            <li>   Reconocer la necesidad permanente de conocimiento adicional y tener la habilidad para localizar, evaluar, integrar y aplicar este conocimiento adecuadamente.  </li>

            <li>   Trabajar efectivamente en equipos que establecen metas, planean tareas, cumplen fechas límite y analizan riesgos e incertidumbre. </li>

        </ul>
        <hr>



        <h2>PERFIL DE EGRESO  </h2>
        <hr class="red">
        <ul>
            <li>      Ejerce su profesión para resolver problemas en su ámbito, trabajando en equipos interdisciplinarios y multiculturales, con liderazgo, sentido crítico, disposición al cambio y comprometido con la calidad.  </li>

            <li>    Diseña y selecciona equipos y procesos para el aprovechamiento sustentable de los recursos bióticos.   </li>

            <li>    Identifica y aplica tecnologías emergentes relacionadas con el campo de acción del Ingeniero Bioquímico para la mejora de procesos existentes.   </li>
            <li>    Participa en el diseño y la aplicación de normas y programas para la gestión y aseguramiento de la calidad, en empresas e instituciones del ámbito de la Ingeniería Bioquímica.   </li>
            <li>    Formula y evalúa proyectos de Ingeniería Bioquímica para coadyuvar al desarrollo regional con criterios de sustentabilidad.   </li>
            <li>     Participa en proyectos de investigación científica y tecnológica en el campo de la Ingeniería Bioquímica para contribuir al desarrollo de la sociedad.  </li>
            <li>     Crea y administra, empresas productoras de bienes y servicios para satisfacer necesidades en el campo de aplicación de la Ingeniería Bioquímica.  </li>



        </ul>

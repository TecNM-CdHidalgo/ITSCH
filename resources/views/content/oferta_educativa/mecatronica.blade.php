                <h2><strong>¿QUÉ ES?</strong></h2>
                <hr class="red">

                <p>Es una Ingeniería donde se fusionan los conocimientos de la Mecánica (materiales, máquinas y mecanismos), de la Electrónica (Control de máquinas a través de circuitos eléctricos) y de los Sistemas Computacionales (programación de las máquinas a través de una computadora para que realicen una tarea específica).
                </p>


                <h2><strong>Objetivo</strong></h2>
                <hr class="red">
                <p>Formar ingenieros que dominen las diferentes disciplinas que conforman a la mecatrónica, Así mismo, contar con un programa académico flexible con un enfoque centrado en el aprendizaje, que permita a los estudiantes construir su propio conocimiento de forma ética y responsable.

                </p>



                <h2><strong>¿Qué necesito para estudiar ingeniería mecatrónica?</strong></h2>
            <hr class="red">
               <p> Es necesario haber cursado el bachillerato de Físico-Matemáticas o equivalente, además tener el gusto por la innovación y la tecnología de vanguardia, ya que se cursarán materias relacionadas con el diseño y programación de robots. Es importante también saber trabajar en equipo, ser una persona perseverante, autodidacta y con iniciativa para la resolución de problemas y desarrollo de proyectos de ingeniería.</p>
            </p>
                <h2><strong>Campo de trabajo</strong></h2>
            <hr class="red">
            <p>El campo de trabajo del ingeniero en mecatrónica es muy amplio y se puede desempeñar en empresas de la industria automotriz, manufacturera, automatización industrial, metal-mecánica, alimentos y electromecánica, principalmente realizando actividades de diseño, manufactura, programación de componentes y equipo especializado, así como en desarrollo de empresas de servicios profesionales.
                </p>
                <hr>



                <h2><strong> Atributos y Objetivos Educacionales de Ing. Mecatrónica </strong></h2>
            <hr class="red">
                <h2><strong> Atributos del egresado </strong></h2>
            <hr class="red">


                <p align="Justify">1.- Identificar, formular y resolver problemas complejos de ingeniería mecatrónica aplicando los principios de las ciencias básicas e ingeniería. </p>
                <p align="Justify">2.- Aplicar, analizar y sintetizar procesos de diseño de ingeniería mecatrónica que resulten en proyectos que cumplen las necesidades especificadas. </p>
                <p align="Justify">3.- Desarrollar y conducir una experimentación adecuada; analizar e interpretar datos y utilizar el juicio ingenieril para establecer conclusiones. </p>
                <p align="Justify">4.- Comunicarse efectivamente con diferentes audiencias. </p>
                <p align="Justify">5.- Reconocer sus responsabilidades éticas y profesionales en situaciones relevantes para la ingeniería mecatrónica y realizar juicios informados, que consideren el impacto de las soluciones de ingeniería en los contextos global, económico, ambiental y social. </p>
                <p align="Justify">6.- Reconocer la necesidad permanente de conocimiento adicional y tener la habilidad para localizar, evaluar, integrar y aplicar este conocimiento adecuadamente. </p>
                <p align="Justify">7.- Trabajar efectivamente en equipos que establecen metas, planean tareas, cumplen fechas límite y analizan riesgos e incertidumbre. </p>


                <h2><strong> Objetivos educacionales </strong></h2>
            <hr class="red">

                <p align="Justify">1.- Los egresados dan solución a problemas en la industria desarrollando sistemas mecatrónicos.</p>
                <p align="Justify">2.- Los egresados tienen puestos de liderazgo en organizaciones públicas y privadas.</p>
                <p align="Justify">3.- Los egresados integran tecnología para desarrollar sistemas mecatrónicos.</p>
                <p align="Justify">4.- Los egresados se especializan en alguna de las diferentes disciplinas de la ingeniería mecatrónica.</p>



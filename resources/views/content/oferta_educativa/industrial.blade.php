                <h2><strong>Misión del programa educativo de Ingeniería Industrial</strong></h2></strong></h2>
<hr class="red">
        <p>
                    “Formar ingenieros industriales con valores y sentido humano capaces de contribuir a la transformación y
                    desarrollo del entorno, utilizando las técnicas adecuadas para el mejoramiento de la sociedad y el medio ambiente”.
                </p>

                <h2><strong>Visión del programa educativo de Ingeniería Industrial</strong></h2>
                <hr class="red">
        <p>
                    “Ser una carrera de excelencia educativa a nivel nacional e internacional que cuente con catedráticos, especialidades
                    e instalaciones de vanguardia e interactúe con los sectores productivos y educativos”
                </p>

                <h2><strong>Política del programa educativo de Ingeniería Industrial</strong></h2>
                <hr class="red">
        <p>
                    “Preparar profesionales capaces de aportar soluciones a las necesidades de los distintos sectores, fomentando la eficacia
                    y eficiencia de los sistemas y procesos, teniendo siempre el compromiso permanente de mejora continua, seguridad y sustentabilidad ambiental.”
                </p>

                <h2><strong>Objetivo General del programa educativo de Ingeniería Industrial</strong></h2>

                <hr class="red">
        <p>
                    Formar profesionistas en el campo de ingeniería industrial, líderes, creativos y emprendedores con capacidad
                    analítica y competitiva que le permita diseñar, implementar, mejorar, innovar, optimizar y administrar sistemas
                    de producción de bienes y servicios en un entorno global.
                </p>

                <h2><strong>Objetivo especialidad del programa educativo de Ingeniería Industrial</strong></h2>
                <hr class="red">
        <p>
                    Formar profesionales enfocados al diseño óptimo de los sistemas esbeltos para la mejora continua, con habilidades
                    para desarrollar y mejorar los sistemas, mediante la administración de los recursos, aplicando herramientas, técnicas
                    y modelos para la toma de decisiones.
                </p>

                <h2><strong>Perfil de ingreso para del programa educativo de Ingeniería Industrial</strong></h2>
                <hr class="red">
                <ol>
                    <li>Poseer conocimientos, habilidades y aptitudes que le permitan un paso sólido durante su estancia y una conclusión exitosa del Plan de Estudios.</li>
                    <li>Tener conocimiento a nivel medio superior de ciencias básicas: Matemáticas, Física y Química.</li>
                    <li>Poseer creatividad e ingenio así como una mentalidad analítica critica.</li>
                    <li>Manejar los instrumentos elementales de medición.</li>

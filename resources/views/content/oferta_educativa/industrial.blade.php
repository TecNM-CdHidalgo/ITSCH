                <h2><strong>Perfil de egreso</strong></h2>
<hr class="red">

                <hr class="red">
        <p>
                    El INGENIERO INDUSTRIAL con especialidad en SISTEMAS ESBELTOS es un profesionista que puede
                    incorporarse a cualquier sistema productivo de bienes o servicios, tanto a empresas que utilicen
                    tecnología de punta como a empresas cuyo nivel tecnológico sea incipiente, logrando con ello generar
                    mejoras significativas.
                </p>

        <p>
                    Es un profesionista versátil, gracias a la gran variedad de asignaturas que fortalecen su formación
                    integral mediante conocimientos en distintas áreas como son: ciencias de ingeniería, ingeniería aplicada,
                    económico-administrativas, considerando hasta aspectos tanto social como humanista; por tal motivo el ingeniero
                    industrial egresado podrá desenvolverse en cualquier institución o empresa sin importar el giro industrial,
                    desde capacitador independiente en industrial manufactureras, maquiladoras y empresas de servicios como hospitales,
                    educativas, financieras, gubernamentales, entre otras, también tendrá la capacidad de formar su propia empresa o negocio,
                    así mismo desenvolverse como empresario en el sector de servicios, asesor y/o consultor, investigador y administrador de empresas.
                    Es flexible que en donde existan personas, métodos de trabajo, procesos manufactureros, productivos o servicios al cliente puede realizar mejoras.
                </p>
                <br> <br>
                <ul>
                    <li>Analizar, diseñar y gestionar sistemas productivos con tecnologías de automatización, sistemas de mantenimiento, tecnologías de información y comunicación, desde la provisión de insumos hasta la entrega de bienes y servicios, considerando factores ergonómicos, planteamientos y modelos analíticos para la mejora de sistemas productivos y de servicios.</li>
                    <li>Trabajar en la mejora de sistemas productivos y de servicios en equipos multidisciplinarios que den solvencia a necesidad específicas del entorno global. </li>
                    <li>Identificar necesidades de su entorno y desarrollar investigación aplicada para crear, mejorar e innovar bienes y/o servicios bajo los principios de productividad y mejora continua.</li>
                    <li>Mejorar los sistemas productivos y de servicios mediante la implementación de sistemas esbeltos y de gestión integral para satisfacer los requerimientos del cliente.</li>
                    <li>Mantener habilidades comunicativas para transmitir con claridad y congruencia sus ideas con los demás.</li>
                    <li>Seleccionar, interpretar e implementar métodos estadísticos en los procesos organizacionales para su mejora y optimización.</li>
